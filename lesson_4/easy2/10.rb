# What are the benefits of using Object Oriented Programming in Ruby?
# Think of as many as you can.

# Using OOP in Ruby has a wide range of benefits. It allows us to better 
# encapsulate and control access to state and behavior, which makes code
# more maintainable and more secure. Using OOP makes it easier to employ
# the DRY principle, via class and interface inheritance relationships.
# By utilizing OOP principles, we can create new interfaces that
# obscure implementation details, and increase the flexibility of objects
# by defining custom classes and methods.

# When put into practice, OOP allows developers to implement more intuitive
# design practices, by leveraging similarities and differences between objects.
